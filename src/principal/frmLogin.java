    private void SetImageButton(JButton ButtonName, String img){
        ImageIcon newImage = new ImageIcon(img);
        Icon icono=new ImageIcon(newImage.getImage().getScaledInstance(ButtonName.getWidth()/4, ButtonName.getHeight(),Image.SCALE_SMOOTH));
        ButtonName.setIcon(icono);
        this.repaint();
        }
    
